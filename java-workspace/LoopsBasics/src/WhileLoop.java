
public class WhileLoop {
	public static void main(String[] args) {
		/*
		 * while(boolean expression){
		 *   some instructions
		 * }
		 * 
		 */
		
		int x = 0; 
		while( x <= 100){
			System.out.println(x);
			x++;
		}	
		//x = 101
		/* Output to console:
		 * 0
		 * 1
		 * ...
		 * 99
		 * 100
		 */
		
		 
	}
}
